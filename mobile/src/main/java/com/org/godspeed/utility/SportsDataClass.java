package com.org.godspeed.utility;

public class SportsDataClass {

    private String id, sports_name, dateSports;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDateSports() {
        return dateSports;
    }

    public void setDateSports(String dateSports) {
        this.dateSports = dateSports;
    }

    public String getSports_name() {
        return sports_name;
    }

    public void setSports_name(String sports_name) {
        this.sports_name = sports_name;
    }
}
